CREATE TABLE PRODUTO(
	id           serial,
	titulo       varchar(50),
	subtitulo    varchar(50),
	isbn         varchar(50),
	paginas      varchar(50),
	edicao       varchar(50),
	editora      varchar(50),
	ano          varchar(50),
	assunto      varchar(50),
	idioma       varchar(50),
	codigoBarras varchar(50)
	);